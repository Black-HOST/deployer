	to_bool() # cast config option into bool
	{
		case "${1:-}" in
			1|true|TRUE|yes|YES|on|ON) echo "true";;
			*) echo "false";;
		esac
	}

# loggers
	log()  { echo "[DEPLOYER] $*"; }
	err()  { echo "[DEPLOYER] Error: $*" >&2; }
	die()  { err "$@"; exit 1; }